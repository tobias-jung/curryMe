			<b>Mit dem Zug</b><br>

				Fahren Sie bis Mannheim Hauptbahnhof. Von dort aus erreichen Sie uns mit der Straßenbahnlinie 5 in Richtung Heidelberg – Weinheim – Viernheim bis Haltestelle „Duale Hochschule“. Fahrtdauer ca. 22 Minuten.
			<br><br>
			<b>Mit öffentlichen Verkehrsmitteln</b><br>

				Mit den Bahnen 6 und 9, oder den Bussen 45 und 50 bis Haltestelle „Neuostheim“.<br>

				Dort umsteigen in die Straßenbahnlinie 5 in Richtung Heidelberg – Weinheim – Viernheim bis Haltestelle „Duale Hochschule“. Direkt an der Haltestelle befindet sich eine Fußgänger-Brücke. Diese führt Sie zum Eingang C.
<br>
				Auf der Website des Verkehrsverbund Rhein-Neckar können Sie eine individuelle Routenplanung, inkl. Fußwege, erstellen.				
